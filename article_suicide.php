      <h2 class="text-center heading mb-4">About Suicide</h2>
        <h4 class="mb-3">Signs of Suicidal Person</h4>
          <ul>
            <li>
              <p class="justify p_color">Direct and indirect verbal expressions: “I don’t want to live anymore”, 
                                           “there is nothing to live for anymore”, “people will be better off without me”.
              </p>
            </li>
            <li>
              <p class="justify p_color">Expressions of hopelessness, helplessness or worthlessness.
              </p>
            </li>
            <li>
              <p class="justify p_color">Withdrawal from family, friends, and interests.
              </p>
            </li>
            <li>
              <p class="justify p_color">Preparation to die – means and plans ; giving away prize possessions and/or 
                                           making a will; tidying up personal affairs; writing notes; making notes on belongings.
              </p>
            </li>
            <li>
              <p class="justify p_color">Saying goodbyes - Reconnecting with old friends and extended family as if to say 
                                           goodbye; Previous unresolved or recent suicide attempt(s).
              </p>
            </li>
            <li>
              <p class="justify p_color">Unusual happiness and peace after an intense period of turmoil and displaying the above characteristics.
              </p>
            </li>
            <li>
              <p class="justify p_color">Substance abuse.
              </p>
            </li>

          </ul>   
          <h4 class="mb-3">How to Talk to Someone Thinking of Suicide</h4>
          <ul>
            <li>
              <p class="justify p_color">Listen to the distressed person with compassion and try to see the world
                                           as they see it to understand the distress.
              </p>
            </li> 
            <li>
              <p class="justify p_color">Giving advice in this state might not be effective.
              </p>
            </li>
            <li>
              <p class="justify p_color">If the person is suicidal don’t leave them alone and remove any means
                                           that could be used for suicide.
              </p>
            </li>
            <li>
              <p class="justify p_color">Take the person to the hospital or emergency room and tell the staff that they are acutely suicidal.
              </p>
            </li>
            <li>
              <p class="justify p_color">Call a suicide prevention helpline.
              </p>
            </li>
            <li>
              <p class="justify p_color">Seek professional help from psychologist, counsellor, psychiatrist, or any other healing professional.
              </p>
            </li>
          </ul>  

          <h4 class="mb-3">Myths and Facts About Suicide </h4>
          <div class="ml-4">
            <p class="justify p_color r_color"><b>Myth :</b> Suicide is always due to mental illness.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> Most people have thought of suicide from time to time and not all people who die by suicide have mental 
                                 health problems. Suicidal behavior indicates deep unhappiness but not necessarily a mental illness. 
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> People who talk about suicide aren’t serious and won’t go through with it.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> People who kill themselves have often told someone that they do not feel life is worth living or that 
                                 they have no future. Some may have actually said they want to die. Someone talking about suicide is a cry for
                                 help, it’s vitally important to take anybody who talks about feeling suicidal seriously. 
                                 The majority of people who feel suicidal do not actually want to die; they do not want to live the life they have.
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> Once a person has made a serious suicide attempt, that person is unlikely to make another.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> People who have tried to end their lives before are significantly more likely to make another attempt.
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> If a person is serious about killing themselves then there is nothing you can do.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> Often, feeling actively suicidal is temporary, even if someone has been feeling low, anxious or struggling to cope
                                 for a long period of time. This is why getting the right kind of support at the right time is so important.
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> Talking about suicide is a bad idea as it may give someone the idea to try it.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> Suicide can be a taboo topic in society. Often, people feeling suicidal don’t want to worry or burden 
                                 anyone with how they feel and so they don’t discuss it. By asking directly about suicide you give them 
                                 permission to tell you how they feel. People who have felt suicidal will often say what a huge relief it is 
                                 to be able to talk about what their experiencing. Once someone starts talking they’ve got a better chance of 
                                 discovering other options to suicide.
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> People who threaten suicide are just attention seeking and shouldn’t be taken seriously.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> People who threaten suicide should always be taken seriously. It may well be that they want attention 
                                 in the sense of calling out for help, and giving them this attention may save their life.
            </p>
            <p class="justify p_color r_color"><b>Myth :</b> People who are suicidal want to die.
            </p>
            <p class="justify p_color g_color"><b>Fact :</b> The majority of people who feel suicidal do not actually want to die; they do not want to live 
                                 the life they have. The distinction may seem small but is in fact very important and is why talking through 
                                 other options at the right time is so vital.
            </p>
          </div>
